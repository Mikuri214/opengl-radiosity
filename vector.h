#include <cmath>

namespace vectormath
{
	struct vector
	{
		float _x;
		float _y;
		float _z;
		float _length;

		vector();
		vector(float x, float y, float z);
	};
	
	struct point
	{
		float _x;
		float _y;
		float _z;

		point();
		point(float x, float y, float z);
	};
	vector * crossproduct(vector*, vector*);
	float dotproduct(vector a, vector b);
	vector * add(point * a, vector * b);
}
